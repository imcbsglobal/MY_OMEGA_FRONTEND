import React, { useState, useEffect } from "react";

// Import your API client - adjust the path as needed
import api from "../../api/client";

export default function ExperienceCertificate() {
  const [certificates, setCertificates] = useState([]);
  const [loading, setLoading] = useState(true);
  const [currentPage, setCurrentPage] = useState(1);
  const [searchQuery, setSearchQuery] = useState("");
  const [showGenerateModal, setShowGenerateModal] = useState(false);
  const [selectedCertificate, setSelectedCertificate] = useState(null);
  const itemsPerPage = 25;

  useEffect(() => {
    loadCertificates();
  }, []);

  const loadCertificates = async () => {
    try {
      const res = await api.get("/certificate/experience-certificates/");
      setCertificates(res?.data?.data || []);
    } catch (err) {
      console.error("Error loading certificates:", err);
    }
    setLoading(false);
  };

  const handleAddNewClick = () => {
    // Navigate to add form
    alert('Navigate to /experience-certificate/add');
  };

  const handleEditClick = (id) => {
    // Navigate to edit form
    alert(`Navigate to /experience-certificate/edit/${id}`);
  };

  const handleGenerateClick = (certificate) => {
    setSelectedCertificate(certificate);
    setShowGenerateModal(true);
  };

  const handleCloseGenerate = () => {
    setShowGenerateModal(false);
    setSelectedCertificate(null);
  };

  const handlePrint = () => {
    window.print();
  };

  const handleDeleteClick = async (id) => {
    if (window.confirm("Are you sure you want to delete this record?")) {
      try {
        await api.delete(`/certificate/experience-certificates/${id}/`);
        setCertificates((prev) => prev.filter((cert) => cert.id !== id));
        alert("Certificate deleted successfully!");
      } catch (err) {
        console.error("Delete error:", err);
        alert("Failed to delete certificate");
      }
    }
  };

  const filteredCertificates = certificates.filter(certificate => {
    if (!searchQuery.trim()) return true;
    const q = searchQuery.toLowerCase().trim();
    return (
      certificate.emp_name?.toLowerCase().includes(q) ||
      certificate.emp_designation?.toLowerCase().includes(q) ||
      certificate.emp_address?.toLowerCase().includes(q)
    );
  });

  const totalPages = Math.ceil(filteredCertificates.length / itemsPerPage);
  const startIndex = (currentPage - 1) * itemsPerPage;
  const endIndex = startIndex + itemsPerPage;
  const currentCertificates = filteredCertificates.slice(startIndex, endIndex);

  useEffect(() => {
    setCurrentPage(1);
  }, [searchQuery]);

  const handlePreviousPage = () => {
    setCurrentPage(prev => Math.max(prev - 1, 1));
  };

  const handleNextPage = () => {
    setCurrentPage(prev => Math.min(prev + 1, totalPages));
  };

  const handlePageClick = (pageNumber) => {
    setCurrentPage(pageNumber);
  };

  const formatDate = (dateString) => {
    if (!dateString) return "N/A";
    const d = new Date(dateString);
    const day = d.getDate();
    const month = d.toLocaleString('en-US', { month: 'long' });
    const year = d.getFullYear();
    return `${day} ${month} ${year}`;
  };

  const formatDateShort = (dateString) => {
    if (!dateString) return new Date().toLocaleDateString('en-GB');
    return new Date(dateString).toLocaleDateString('en-GB');
  };

  if (loading) {
    return (
      <div style={{...styles.container, display: 'flex', justifyContent: 'center', alignItems: 'center', minHeight: '100vh'}}>
        <div style={{fontSize: '18px', color: '#6b7280'}}>Loading certificate data...</div>
      </div>
    );
  }

  return (
    <div style={styles.container}>
      <div style={styles.header} className="no-print">
        <h2 style={styles.title}>Experience Certificate Management</h2>
        <div style={styles.headerActions}>
          <div style={styles.searchContainer}>
            <input
              type="text"
              placeholder="Search by name, job title, address..."
              value={searchQuery}
              onChange={(e) => setSearchQuery(e.target.value)}
              style={styles.searchInput}
            />
            <span style={styles.searchIcon}>üîç</span>
          </div>
          <button onClick={handleAddNewClick} style={styles.addButton}>
            + Add New
          </button>
        </div>
      </div>

      <div style={styles.tableContainer} className="no-print">
        <table style={styles.table}>
          <thead>
            <tr style={styles.tableHeaderRow}>
              <th style={styles.tableHeader}>SL NO</th>
              <th style={styles.tableHeader}>NAME</th>
              <th style={styles.tableHeader}>EMAIL</th>
              <th style={styles.tableHeader}>JOB TITLE</th>
              <th style={styles.tableHeader}>JOINING DATE</th>
              <th style={styles.tableHeader}>ISSUE DATE</th>
              <th style={styles.tableHeader}>GENERATED BY</th>
              <th style={styles.tableHeader}>ACTION</th>
            </tr>
          </thead>
          <tbody>
            {currentCertificates.length === 0 ? (
              <tr>
                <td colSpan="8" style={styles.noResults}>
                  {searchQuery ? `No results found for "${searchQuery}"` : "No certificate records available"}
                </td>
              </tr>
            ) : (
              currentCertificates.map((certificate, idx) => (
                <tr key={certificate.id} style={styles.tableRow}>
                  <td style={styles.tableCell}>{startIndex + idx + 1}</td>
                  <td style={styles.tableCell}>{certificate.emp_name}</td>
                  <td style={styles.tableCell}>{certificate.emp_email || "N/A"}</td>
                  <td style={styles.tableCell}>{certificate.emp_designation}</td>
                  <td style={styles.tableCell}>{certificate.joining_date || "N/A"}</td>
                  <td style={styles.tableCell}>{certificate.issue_date || "N/A"}</td>
                  <td style={styles.tableCell}>{certificate.generated_by_name || "N/A"}</td>
                  <td style={styles.tableCell}>
                    <div style={styles.actionButtons}>
                      <button onClick={() => handleGenerateClick(certificate)} style={styles.generateBtn}>Generate</button>
                      <button onClick={() => handleEditClick(certificate.id)} style={styles.editBtn}>Edit</button>
                      <button onClick={() => handleDeleteClick(certificate.id)} style={styles.deleteBtn}>Delete</button>
                    </div>
                  </td>
                </tr>
              ))
            )}
          </tbody>
        </table>
      </div>

      <div style={styles.paginationContainer} className="no-print">
        <div style={styles.paginationInfo}>
          Showing {filteredCertificates.length > 0 ? startIndex + 1 : 0} to {Math.min(endIndex, filteredCertificates.length)} of {filteredCertificates.length} entries
          {searchQuery && <span style={styles.searchIndicator}> (filtered from {certificates.length} total)</span>}
        </div>
        <div style={styles.paginationButtons}>
          <button 
            onClick={handlePreviousPage} 
            disabled={currentPage === 1}
            style={{...styles.paginationBtn, ...(currentPage === 1 ? styles.paginationBtnDisabled : {})}}
          >
            Previous
          </button>
          
          <div style={styles.pageNumbers}>
            {Array.from({ length: totalPages }, (_, i) => i + 1).map(pageNum => {
              if (
                pageNum === 1 || 
                pageNum === totalPages || 
                (pageNum >= currentPage - 1 && pageNum <= currentPage + 1)
              ) {
                return (
                  <button
                    key={pageNum}
                    onClick={() => handlePageClick(pageNum)}
                    style={{
                      ...styles.pageNumberBtn,
                      ...(pageNum === currentPage ? styles.pageNumberBtnActive : {})
                    }}
                  >
                    {pageNum}
                  </button>
                );
              } else if (pageNum === currentPage - 2 || pageNum === currentPage + 2) {
                return <span key={pageNum} style={styles.pageEllipsis}>...</span>;
              }
              return null;
            })}
          </div>

          <button 
            onClick={handleNextPage} 
            disabled={currentPage === totalPages}
            style={{...styles.paginationBtn, ...(currentPage === totalPages ? styles.paginationBtnDisabled : {})}}
          >
            Next
          </button>
        </div>
      </div>

      {showGenerateModal && selectedCertificate && (
        <div style={styles.modalOverlay}>
          <div style={styles.generateModal}>
            <div style={styles.modalHeader} className="no-print">
              <h2 style={styles.modalTitle}>Experience Certificate</h2>
              <button onClick={handleCloseGenerate} style={styles.closeButton}>‚úï</button>
            </div>

            <div style={styles.certificateContainer}>
              <div style={styles.certificatePage}>
                {/* Header */}
                <div style={styles.certificateHeader}>
                  <div style={styles.headerLeft}>
                    <img 
                      src="/assets/omega-logo.png" 
                      alt="Omega Logo" 
                      style={styles.headerLogo}
                      onError={(e) => e.target.style.display = 'none'}
                    />
                  </div>
                  <div style={styles.headerRight}>
                    <h1 style={styles.companyName}>BASIL ENTERPRISES</h1>
                    <p style={styles.companyAddress}>
                      Omega Trade centre, Opp City Hospital<br />
                      Mavoor Road, Kozhikode,<br />
                      Malappuram Dt Kerala, India
                    </p>
                    <div style={styles.contactInfo}>
                      <span>Mob: 9961 282 899</span>
                      <span style={{marginLeft: '15px'}}>L-L: 0494 242 5702</span>
                    </div>
                    <p style={styles.gstInfo}>GSTIN : 32AEKPK8326P1Z8</p>
                  </div>
                </div>

                <div style={styles.blueLine}></div>

                {/* Ref and Date */}
                <div style={styles.refDateSection}>
                  <div>REF NO: {selectedCertificate.ref_no || "BE/HR/EXP/2025/123"}</div>
                  <div>{formatDateShort(selectedCertificate.issue_date)}</div>
                </div>

                {/* To Whom */}
                <div style={styles.toWhomSection}>
                  <p style={styles.toWhomText}>TO WHOMOSVER IT MAY CONCERN.</p>
                </div>

                {/* Watermark */}
                <div style={styles.watermark}>
                  <img 
                    src="/assets/omega-logo.png" 
                    alt="" 
                    style={styles.watermarkImage}
                    onError={(e) => e.target.style.display = 'none'}
                  />
                </div>

                {/* Content */}
                <div style={styles.mainContent}>
                  <p style={styles.employeeName}>
                    Mr. {selectedCertificate.emp_name || "EMPLOYEE NAME"}
                  </p>
                  
                  <p style={styles.bodyText}>
                    This is certify that Mr. /so Mr. A. JOHN, /so Mr. A. JOHN, was Basil Enterprises as the {selectedCertificate.emp_designation || "Business Development Manager"} as the dalyess employed with from {formatDate(selectedCertificate.joining_date)} to {selectedCertificate.emp_designation || "Manager"} from {formatDate(selectedCertificate.joining_date)}, to {formatDate(selectedCertificate.end_date)}.
                  </p>

                  <p style={styles.bodyText}>
                    During his teniure provides 12 Paidra dedication of proficiency. We wish the future endadours.
                  </p>
                </div>

                {/* Signature */}
                <div style={styles.signatureSection}>
                  <p style={styles.forCompany}>For BASIL ENTERPRISES</p>
                  <div style={{height: '50px'}}></div>
                  <p style={styles.authorizedSign}>Authorized Signistary</p>
                </div>

                {/* Footer */}
                <div style={styles.footer}>
                  <div style={styles.blueFooterLine}></div>
                  <p style={styles.footerText}>
                    www.myomega.in ~ email: hr@myomeja
                  </p>
                </div>
              </div>

              {/* Print Button */}
              <div style={styles.bottomActions} className="no-print">
                <button onClick={handlePrint} style={styles.bottomPrintButton}>
                  üñ®Ô∏è Print Certificate
                </button>
              </div>
            </div>
          </div>
        </div>
      )}
    </div>
  );
}

